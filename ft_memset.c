#include "libft.h"

void	*ft_memset(void *s1, int c, size_t n)
{
	size_t	i;
	char	*str;

	i = 0;
	str = s1;
	while (i < n)
	{
		str[i] = c;
		i++;
	}
	return (s1);
}
